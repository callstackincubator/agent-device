export async function buildKnowledgeFiles() {
  const commands = buildCommandManifest();
  const files = new Map();
  files.set('knowledge/index.md', renderIndex(commands));
  files.set('knowledge/platform-matrix.md', renderPlatformMatrix(commands));
  files.set('knowledge/agent-routes.md', renderAgentRoutes());
  files.set('knowledge/index.json', renderIndexJson(commands));
  for (const command of commands) {
    files.set(`knowledge/commands/${command.name}.md`, renderCommandPage(command));
  }

  const expectedPaths = new Set(
    [...files.keys()].map((relativePath) => path.join(ROOT_DIR, relativePath)),
  );
  for (const [relativePath] of files) {
    if (relativePath.endsWith('.md')) {
      const fullPath = path.join(ROOT_DIR, relativePath);
      const links = [...normalizeText(files.get(relativePath)).matchAll(/\]\(([^)]+)\)/g)].map(
        (match) => match[1],
      );
      for (const link of links) {
        if (link.startsWith('./') || link.startsWith('../')) {
          const resolved = path.resolve(path.dirname(fullPath), link);
          if (expectedPaths.has(resolved)) continue;
          await fs.access(resolved);
        }
      }
    }
  }
  return files;
}

export async function writeKnowledgeFiles(files) {
  await fs.rm(KNOWLEDGE_DIR, { force: true, recursive: true });
  for (const [relativePath, content] of files) {
    const outputPath = path.join(ROOT_DIR, relativePath);
    await fs.mkdir(path.dirname(outputPath), { recursive: true });
    await fs.writeFile(outputPath, normalizeText(content), 'utf8');
  }
}

async function listKnowledgeFiles() {
  const files = [];
  async function walk(currentDir) {
    const entries = await fs.readdir(currentDir, { withFileTypes: true });
    for (const entry of entries) {
      const fullPath = path.join(currentDir, entry.name);
      if (entry.isDirectory()) {
        await walk(fullPath);
        continue;
      }
      files.push(path.relative(ROOT_DIR, fullPath).split(path.sep).join('/'));
    }
  }

  try {
    await walk(KNOWLEDGE_DIR);
  } catch (error) {
    if (error && error.code === 'ENOENT') return [];
    throw error;
  }
  return files.sort(compareStrings);
}

export async function checkKnowledgeFiles(files) {
  const expectedPaths = [...files.keys()].sort(compareStrings);
  const actualPaths = await listKnowledgeFiles();
  const missing = expectedPaths.filter((entry) => !actualPaths.includes(entry));
  const extra = actualPaths.filter((entry) => !expectedPaths.includes(entry));
  const changed = [];

  for (const relativePath of expectedPaths) {
    const fullPath = path.join(ROOT_DIR, relativePath);
    let current = null;
    try {
      current = await fs.readFile(fullPath, 'utf8');
    } catch (error) {
      if (error && error.code === 'ENOENT') continue;
      throw error;
    }
    if (current !== null && normalizeText(current) !== normalizeText(files.get(relativePath))) {
      changed.push(relativePath);
    }
  }

  return {
    changed,
    extra,
    missing,
    ok: changed.length === 0 && extra.length === 0 && missing.length === 0,
  };
}
