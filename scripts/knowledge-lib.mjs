const SESSION_REQUIREMENTS = {
  noSessionRequired: new Set([
    'apps',
    'boot',
    'devices',
    'ensure-simulator',
    'install',
    'install-from-source',
    'metro',
    'open',
    'reinstall',
    'session',
  ]),
  managesOwnSessions: new Set(['batch', 'replay', 'test']),
};

function normalizeText(value) {
  return value.replace(/\r\n/g, '\n');
}

function compareStrings(a, b) {
  return a.localeCompare(b, 'en');
}

function getFlagMetadataByKey() {
  const byKey = new Map();
  for (const definition of getFlagDefinitions()) {
    const list = byKey.get(definition.key);
    if (list) list.push(definition);
    else byKey.set(definition.key, [definition]);
  }
  return byKey;
}

function buildFamilyDefinitionMap() {
  const commandToFamily = new Map();
  for (const family of FAMILY_DEFINITIONS) {
    for (const command of family.commands) {
      const existing = commandToFamily.get(command);
      if (existing) {
        throw new Error(`Command ${command} is assigned to multiple knowledge families.`);
      }
      commandToFamily.set(command, family);
    }
  }
  return commandToFamily;
}

function getSessionRequirement(command) {
  if (SESSION_REQUIREMENTS.noSessionRequired.has(command)) {
    return 'Does not require an active session.';
  }
  if (SESSION_REQUIREMENTS.managesOwnSessions.has(command)) {
    return 'Creates or manages sessions internally.';
  }
  if (command === 'close') {
    return 'Requires an active session to close it.';
  }
  return 'Usually expects an active session.';
}

function buildPlatformSupport(command, capabilityCommands) {
  if (!capabilityCommands.has(command)) return null;
  return Object.fromEntries(
    SAMPLE_DEVICES.map((sample) => [
      sample.key,
      isCommandSupportedOnDevice(command, sample.device),
    ]),
  );
}

function formatBool(value) {
  return value ? 'Yes' : 'No';
}

function formatLink(fromPath, targetPath) {
  const relative = path.relative(path.dirname(fromPath), path.join(ROOT_DIR, targetPath));
  return `[${targetPath}](${relative.split(path.sep).join('/')})`;
}

function formatListSection(fromPath, title, entries) {
  if (entries.length === 0) return `## ${title}\n\n- None`;
  return `## ${title}\n\n${entries.map((entry) => `- ${formatLink(fromPath, entry)}`).join('\n')}`;
}

function buildCommandManifest() {
  const flagMetadataByKey = getFlagMetadataByKey();
  const capabilityCommands = new Set(getSchemaCapabilityKeys());
  const familyDefinitionByCommand = buildFamilyDefinitionMap();
  return getCliCommandNames()
    .sort(compareStrings)
    .map((command) => {
      const schema = getCommandSchema(command);
      if (!schema) throw new Error(`Missing schema for command ${command}`);
      const family = familyDefinitionByCommand.get(command) ?? null;
      const allowedFlags = schema.allowedFlags.flatMap((key) => {
        return (flagMetadataByKey.get(key) ?? []).map((definition) => ({
          key: definition.key,
          names: [...definition.names],
          usageDescription: definition.usageDescription ?? '',
          usageLabel: definition.usageLabel ?? definition.names[0],
        }));
      });
      return {
        aliases: COMMAND_ALIASES[command] ?? [],
        allowedFlags,
        capabilityChecked: capabilityCommands.has(command),
        docs: family?.docs ?? ['website/docs/docs/commands.md'],
        family: family?.name ?? 'unclassified',
        helpDescription: schema.helpDescription,
        name: command,
        notes: COMMAND_NOTES[command] ?? [],
        paths: family?.primarySources ?? [],
        platformSupport: buildPlatformSupport(command, capabilityCommands),
        positionals: [...schema.positionalArgs],
        sessionRequirement: getSessionRequirement(command),
        summary: schema.summary ?? schema.helpDescription,
        usage:
          buildCommandUsageText(command)
            ?.split('\n', 1)[0]
            .replace(/^agent-device\s+/, '') ?? command,
      };
    });
}

function renderCommandPage(command) {
  const filePath = path.join(KNOWLEDGE_DIR, 'commands', `${command.name}.md`);
  const aliasBlock =
    command.aliases.length > 0
      ? command.aliases.map((alias) => `- \`${alias}\``).join('\n')
      : '- None';
  const notesBlock =
    command.notes.length > 0 ? command.notes.map((note) => `- ${note}`).join('\n') : '- None';
  const flagsBlock =
    command.allowedFlags.length > 0
      ? command.allowedFlags
          .map((flag) => `- \`${flag.usageLabel}\`: ${flag.usageDescription}`)
          .join('\n')
      : '- None';

  const platformBlock =
    command.platformSupport === null
      ? '_Capability checks are not defined for this command in `command-schema.ts`._'
      : [
          '| Target | Supported |',
          '| --- | --- |',
          ...SAMPLE_DEVICES.map(
            (sample) => `| ${sample.label} | ${formatBool(command.platformSupport[sample.key])} |`,
          ),
        ].join('\n');

  const sections = [
    GENERATED_HEADER,
    '',
    `# ${command.name}`,
    '',
    command.summary,
    '',
    '## Usage',
    '',
    '```bash',
    `agent-device ${command.usage}`,
    '```',
    '',
    '## Session Requirement',
    '',
    command.sessionRequirement,
    '',
    '## Aliases',
    '',
    aliasBlock,
    '',
    '## Notes',
    '',
    notesBlock,
    '',
    '## Allowed Flags',
    '',
    flagsBlock,
    '',
    '## Platform Coverage',
    '',
    platformBlock,
    '',
    formatListSection(filePath, 'Primary Source Paths', command.paths),
    '',
    formatListSection(filePath, 'Docs And Skills', command.docs),
  ];
  return `${sections.join('\n')}\n`;
}

function renderIndex(commands) {
  const filePath = path.join(KNOWLEDGE_DIR, 'index.md');
  const commandLines = commands
    .map(
      (command) =>
        `| \`${command.name}\` | ${command.summary} | [page](./commands/${command.name}.md) |`,
    )
    .join('\n');
  const sourceLinks = [
    'AGENTS.md',
    'src/utils/command-schema.ts',
    'src/core/capabilities.ts',
    'skills/agent-device/SKILL.md',
    'website/docs/docs/commands.md',
  ]
    .map((entry) => `- ${formatLink(filePath, entry)}`)
    .join('\n');

  return `${GENERATED_HEADER}

# Knowledge Index

This directory is the generated agent-facing map of the repo. The source of truth stays in code, docs, and skills; \`pnpm kb:build\` compiles that material into a cheaper starting point for exploration and drift checks.

## Primary Sources

${sourceLinks}

## Refresh Workflow

1. Run \`pnpm kb:build\` after changing command schema, capabilities, docs, or skills.
2. Run \`pnpm kb:check\` in CI or before merging to catch stale generated files.
3. Start investigation here, then jump to the linked source files for the owning command family.

## Command Index

| Command | Summary | Page |
| --- | --- | --- |
${commandLines}
`;
}

function renderPlatformMatrix(commands) {
  const rows = commands
    .map((command) => {
      if (command.platformSupport === null) {
        return `| \`${command.name}\` | n/a | n/a | n/a | n/a | n/a | n/a | n/a |`;
      }
      return (
        [
          `| \`${command.name}\``,
          formatBool(command.platformSupport.iosSimulator),
          formatBool(command.platformSupport.iosDevice),
          formatBool(command.platformSupport.tvSimulator),
          formatBool(command.platformSupport.macosDesktop),
          formatBool(command.platformSupport.androidEmulator),
          formatBool(command.platformSupport.androidDevice),
          formatBool(command.platformSupport.androidTv),
        ].join(' | ') + ' |'
      );
    })
    .join('\n');

  return `${GENERATED_HEADER}

# Platform Matrix

Generated from \`src/core/capabilities.ts\` by evaluating each command against a small set of representative device targets.

| Command | iOS sim | iOS device | tvOS sim | macOS | Android emu | Android device | Android TV |
| --- | --- | --- | --- | --- | --- | --- | --- |
${rows}
`;
}

function renderAgentRoutes() {
  const filePath = path.join(KNOWLEDGE_DIR, 'agent-routes.md');
  const sections = ROUTES.map((route) => {
    const paths = route.paths.map((entry) => `- ${formatLink(filePath, entry)}`).join('\n');
    return `## ${route.task}\n\n${paths}`;
  }).join('\n\n');

  return `${GENERATED_HEADER}

# Agent Routes

Use this page when deciding where to read first. The goal is to keep the first hop narrow and close to the owning module.

${sections}
`;
}

